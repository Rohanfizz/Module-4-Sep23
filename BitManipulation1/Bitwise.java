import java.util.*;

class Bitwise {

  public static void main(String[] args) {
    Scanner scn = new Scanner(System.in);
    System.out.println(10 & 12);
    System.out.println(10 | 12);
    System.out.println(10 ^ 12);
    System.out.println(11 >> 3); //11/2 => 5/2 => 2/2 => 1
    scn.close();
  }
}
